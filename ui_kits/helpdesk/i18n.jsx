function getNested(obj, path) {
  return path.split('.').reduce((o, k) => (o && o[k] != null ? o[k] : null), obj);
}

function formatTemplate(str, vars = {}) {
  if (!str) return '';
  let s = str;
  const n = vars.n != null ? vars.n : 0;
  s = s.replace(/\{s\}/g, n === 1 ? '' : 's');
  s = s.replace(/\{n\}/g, String(n));
  Object.keys(vars).forEach(k => {
    if (k !== 'n') s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
  });
  return s;
}

const I18nContext = React.createContext({ lang: 'fr', setLang: () => {}, t: (k) => k });

function I18nProvider({ children, defaultLang }) {
  const [lang, setLangState] = React.useState(() => {
    try {
      return localStorage.getItem('pmg-lang') || defaultLang || 'fr';
    } catch (e) {
      return defaultLang || 'fr';
    }
  });

  const setLang = (next) => {
    setLangState(next);
    try { localStorage.setItem('pmg-lang', next); } catch (e) {}
    document.documentElement.lang = next;
  };

  React.useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const t = React.useCallback((key, vars) => {
    const raw = getNested(STRINGS[lang], key) ?? getNested(STRINGS.en, key) ?? key;
    return typeof raw === 'string' ? formatTemplate(raw, vars) : raw;
  }, [lang]);

  const value = React.useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

function useI18n() {
  return React.useContext(I18nContext);
}

function getLocalizedCategory(catId, lang) {
  const c = STRINGS[lang]?.categories?.[catId] || STRINGS.en?.categories?.[catId];
  const base = window.PMG_DATA?.CATEGORIES?.find(x => x.id === catId)
    || window.PMG_DATA?.IT_PROCESS_CATEGORIES?.find(x => x.id === catId);
  return {
    ...base,
    id: catId,
    label: c?.label || base?.label || catId,
    hint: c?.hint || base?.hint || '',
    icon: base?.icon,
  };
}

function getLocalizedPortalIncident(portalId, lang) {
  const p = STRINGS[lang]?.portalIncidents?.[portalId] || STRINGS.en?.portalIncidents?.[portalId];
  const base = window.PMG_DATA?.PORTAL_INCIDENT_ITEMS?.find(x => x.id === portalId);
  if (!base && !p) return getLocalizedCategory(portalId, lang);
  return {
    ...base,
    id: portalId,
    label: p?.label || base?.id || portalId,
    hint: p?.hint || '',
    icon: base?.icon || 'tag',
  };
}

function getLocalizedService(serviceId, lang) {
  const keyMap = {
    'special-it': 'specialIt',
    'employee-arrival': 'employeeArrival',
    'employee-departure': 'employeeDeparture',
    'it-equipment': 'itEquipment',
  };
  const key = keyMap[serviceId] || serviceId;
  const s = STRINGS[lang]?.services?.[key] || STRINGS.en?.services?.[key];
  const base = window.PMG_DATA?.SERVICE_CATALOG?.find(x => x.id === serviceId);
  return {
    ...base,
    id: serviceId,
    label: s?.label || base?.label || serviceId,
    description: s?.description || '',
    short: s?.short || base?.hint || '',
    icon: base?.icon || 'clipboard',
  };
}

function getStatusLabel(status, t) {
  const norm = typeof normalizeStatus === 'function' ? normalizeStatus(status) : status;
  return t(`status.${norm}`) || status;
}

function getPriorityLabel(priority, t) {
  return t(`priority.${priority}`) || priority;
}

function LangSwitch({ style }) {
  const { lang, setLang } = useI18n();
  const btn = (code, label) => (
    <button key={code} type="button" onClick={() => setLang(code)}
      style={{
        fontFamily:'inherit', fontSize:11, fontWeight:600, padding:'4px 8px', borderRadius:4,
        border:'1px solid ' + (lang === code ? 'var(--accent-600)' : 'var(--border)'),
        background: lang === code ? 'var(--accent-50)' : 'white',
        color: lang === code ? 'var(--accent-700)' : 'var(--fg-secondary)',
        cursor:'pointer',
      }}>{label}</button>
  );
  return (
    <div style={{display:'inline-flex', gap:4, ...style}} role="group" aria-label="Language">
      {btn('fr', 'FR')}
      {btn('en', 'EN')}
    </div>
  );
}

